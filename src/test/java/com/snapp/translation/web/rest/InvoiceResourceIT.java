    private static final BigDecimal DEFAULT_CUSTOMER_PRICE = new BigDecimal(1);
    private static final BigDecimal UPDATED_CUSTOMER_PRICE = new BigDecimal(2);

    private static final BigDecimal DEFAULT_TOTAL_PRICE = new BigDecimal(1);
    private static final BigDecimal UPDATED_TOTAL_PRICE = new BigDecimal(2);

    private static final BigDecimal DEFAULT_TAX_PRICE = new BigDecimal(1);
    private static final BigDecimal UPDATED_TAX_PRICE = new BigDecimal(2);

    private static final InvoiceStatus DEFAULT_INVOICE_STATUS = InvoiceStatus.PAID;
    private static final InvoiceStatus UPDATED_INVOICE_STATUS = InvoiceStatus.UNPAID;

    private static final String ENTITY_API_URL = "/api/invoices";
    private static final String ENTITY_API_URL_ID = ENTITY_API_URL + "/{id}";

    private static Random random = new Random();
    private static AtomicLong count = new AtomicLong(random.nextInt() + (2 * Integer.MAX_VALUE));

    @Autowired
    private InvoiceRepository invoiceRepository;

